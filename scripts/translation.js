let currentLang = 'en';

function translatePage(lang) {
    currentLang = lang;
    
    document.documentElement.lang = lang;
    
    const langTranslations = translations[lang];
    if (!langTranslations) return;
    
    // Update all translatable text
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = langTranslations[key];
        if (translation) {
            element.textContent = translation;
        }
    });

    document.querySelectorAll('[data-i18n-html]').forEach(element => {
        const key = element.getAttribute('data-i18n-html');
        const translation = langTranslations[key];
        if (translation) {
            element.innerHTML = translation;
        }
    });
    
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        const translation = langTranslations[key];
        
        if (translation) {
            element.placeholder = translation;
        }
    });
    
    document.querySelectorAll('[data-i18n-aria-label]').forEach(element => {
        const key = element.getAttribute('data-i18n-aria-label');
        const translation = langTranslations[key];
        
        if (translation) {
            element.setAttribute('aria-label', translation);
        }
    });
    
    // Update language code display
    const langCodeElements = document.querySelectorAll('.lang-code');
    langCodeElements.forEach(element => {
        if (element.closest('.lang-option')) {
            return;
        }
        element.textContent = lang.toUpperCase();
    });
    
    // Update language picker current language
    const currentLangName = document.querySelector('.lang-current .lang-name');
    if (currentLangName && langTranslations[`lang.${lang}`]) {
        currentLangName.textContent = langTranslations[`lang.${lang}`];
    }
    
    localStorage.setItem('preferredLang', lang);
    
    // Announce language change to screen readers
    const announcement = document.createElement('div');
    announcement.setAttribute('aria-live', 'polite');
    announcement.setAttribute('aria-atomic', 'true');
    announcement.className = 'sr-only';
    announcement.textContent = `Language changed to ${lang === 'en' ? 'English' : lang === 'ar' ? 'Arabic' : 'French'}`;
    document.body.appendChild(announcement);
    setTimeout(() => announcement.remove(), 1000);
    
    console.log(`Language changed to ${lang}`);
}

document.addEventListener('DOMContentLoaded', function() {
    // Detect browser language or use saved preference
    const browserLang = navigator.language.split('-')[0];
    const savedLang = localStorage.getItem('preferredLang') || 
                     (['en', 'ar', 'fr'].includes(browserLang) ? browserLang : 'en');
    
    translatePage(savedLang);
    
    // Set up language switcher
    const langOptions = document.querySelectorAll('.lang-option');
    langOptions.forEach(option => {
        option.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            translatePage(lang);
            
            // Close dropdown
            const languagePickers = document.querySelectorAll('.language-picker');
            languagePickers.forEach(picker => {
                picker.classList.remove('active');
                const langCurrent = picker.querySelector('.lang-current');
                if (langCurrent) langCurrent.setAttribute('aria-expanded', 'false');
            });
        });
        
        // Keyboard support
        option.addEventListener('keydown', function(e) {
            if (e.key === 'Enter' || e.key === ' ') {
                e.preventDefault();
                this.click();
            }
        });
    });
    
    console.log('Translation system initialized');
});